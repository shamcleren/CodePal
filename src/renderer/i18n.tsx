const MESSAGES: Record<ResolvedLocale, MessageDictionary> = {
  en: EN_MESSAGES,
  "zh-CN": ZH_CN_MESSAGES,
};

type I18nValue = {
  locale: ResolvedLocale;
  t: (key: string, params?: MessageParams) => string;
  formatDateTime: (value: number, options?: Intl.DateTimeFormatOptions) => string;
  translateMessage: (fallback: string, key?: string, params?: MessageParams) => string;
};

const I18nContext = createContext<I18nValue>(createI18nValue("en"));

function interpolate(template: string, params?: MessageParams): string {
  if (!params) {
    return template;
  }
  return template.replace(/\{([^}]+)\}/g, (_match, key: string) => {
    const value = params[key];
    return value === undefined ? "" : String(value);
  });
}

export function resolveLocale(setting: AppLocale, systemLocale: string | undefined): ResolvedLocale {
  if (setting === "en" || setting === "zh-CN") {
    return setting;
  }
  return systemLocale?.toLowerCase().startsWith("zh") ? "zh-CN" : "en";
}

export function createI18nValue(locale: ResolvedLocale): I18nValue {
  const dictionary = MESSAGES[locale];
  return {
    locale,
    t(key, params) {
      return interpolate(dictionary[key] ?? key, params);
    },
    formatDateTime(value, options) {
      return new Intl.DateTimeFormat(locale, options).format(new Date(value));
    },
    translateMessage(fallback, key, params) {
      if (!key) {
        return fallback;
      }
      return interpolate(dictionary[key] ?? fallback, params);
    },
  };
}

export function I18nProvider({
  locale,
  children,
}: {
  locale: ResolvedLocale;
  children: ReactNode;
}) {
  const value = createI18nValue(locale);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  return useContext(I18nContext);
}

export function translateWindowLabel(value: string | undefined, t: I18nValue["t"]): string {
  if (!value) {
    return t("usage.window.generic");
  }
  const normalized = value.trim().toLowerCase();
  if (normalized === "月度") return t("usage.window.monthly");
  if (normalized === "总量") return t("usage.window.total");
  if (normalized === "内网") return t("usage.window.internal");
  if (normalized === "enterprise") return t("usage.window.enterprise");

  return value
    .replace(/\s*小时/g, "h")
    .replace(/\s*天/g, "d")
    .replace(/\s+/g, "");
}
